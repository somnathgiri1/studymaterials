CREATE OR REPLACE TRIGGER INSERT_STOCK_RECORDS
after insert on makess.mst_item for each row
DECLARE
 fin_start_date   date;
 fin_end_date     date;
Begin

Begin
  select  dt_current_start_date ,dt_current_end_date
  into  fin_start_date,fin_end_date
  from  fin_account_year
  where  vc_comp_code=:new.vc_comp_code
  and ch_year_closing_flag = 'O';
 exception
  when NO_DATA_FOUND then
  select  dt_current_start_date , dt_current_end_date
  into  fin_start_date,fin_end_date
  from  fin_account_year
  where  vc_comp_code=:new.vc_comp_code
  and ch_year_closing_flag = 'C';

 when others then
  raise_application_error(-20101, 'Error encountered with your   financial year') ;
 End;


 
 Insert into stock_summary
 ( vc_comp_code , vc_item_code, NU_BALANCE_QTY, NU_QTY_REJECTED,
 NU_QTY_REWORKABLE, NU_QTY_DEFECTIVE , NU_QTY_ORDERED, NU_QTY_REQUESTED
 , NU_QTY_TO_ORD, NU_QTY_AVAILABLE , NU_QTY_REQUIRED,vc_default_comp)
 values (:new.vc_comp_code , :new.vc_item_code , NVL(:new.nu_opening_qty,0), 0,
 0, 0, 0, 0,
 0, nvl(:new.nu_opening_qty,0), 0 ,:new.vc_default_comp) ;
--2)
 
 Insert into stk_lot_summary
 ( vc_comp_code , vc_item_code, NU_BALANCE_QTY, NU_QTY_REJECTED,
 NU_QTY_REWORKABLE, NU_QTY_DEFECTIVE, VC_LOT_NO, VC_BIN_NO, NU_COST,vc_default_comp,dt_mod_date,ch_stat_flag)
 values (:new.vc_comp_code , :new.vc_item_code , nvl(:new.nu_opening_qty,0), 0,
  0, 0, :new.vc_lot_no, :new.vc_bin_no, nvl(:new.nu_eval_rate,0),:new.vc_default_comp,sysdate,'IN')  ;

--4)

 Insert into mst_month_summary
 ( vc_comp_code, dt_fin_start_date, dt_fin_end_date, vc_item_code,
 nu_year_open_balance, nu_tot_received, vc_default_comp,dt_mod_date,ch_stat_flag)
 values (:new.vc_comp_code ,fin_start_date,fin_END_date, :new.vc_item_code ,
 nvl(:new.nu_opening_qty,0), nvl(:new.nu_opening_qty,0),
 :new.vc_default_comp,sysdate,'IN')  ;

Exception
 when others then
  raise_application_error(-20101, 'Error encountered ') ;
End ;
